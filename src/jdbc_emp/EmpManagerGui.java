    private void btDaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDaddActionPerformed
        //1) 사용자가 입력한 부서명, 근무지 얻어오기
    	String dname=tdname.getText();
    	String loc=tloc.getText();
    	
    	//2) 널체크, 빈문자열 체크 -유효성 체크
    	if(dname==null||loc==null||dname.trim().isEmpty()||loc.trim().isEmpty()) {
    		//dname.trim().isEmpty() -> trim 앞뒤 공백제거, isEmpty -> 비어있나
    		showMsg("부서명과 부서위치를 입력하세요");
    		tdname.requestFocus();
    		return;
    	}
    	
    	//3) DeptVO객체 생성해서 얻어온 값들 setting
    	DeptVO dvo = new DeptVO();
    	dvo.setDname(dname);
    	dvo.setLoc(loc);
    	
    	//4) deptDao의 insertDept()메소드 호출하기
    	//   그 결과값에 따른 메시지 처리 (등록 성공 or 등록 실패)
    	boolean result = deptDao.insertDept(dvo);
    	String message = (result)? "등록 성공":"등록 실패";
    	showMsg(message);
    	if(result) { // 잘 등록됐다면...부서 테이블 목록 보여주기
    		showAllDeptInfo(); // 정보 모듈화하여 재사용성 높게    		
    	}
    	
    }//GEN-LAST:event_btDaddActionPerformed
    
    public void showAllDeptInfo() { // 정보 모듈화하여 재사용성 높게
    	ArrayList<DeptVO> dlist=deptDao.allDept();
    	showDeptTable(dlist);
    }
    public void showDeptTable(ArrayList<DeptVO> dlist) {
    	Object [][] data = new Object[dlist.size()][3]; // 2차원 배열 생성
        
        // ArrayList에 저장된 데이터를 2차원 배열에 옮기자.
        for(int i=0; i<data.length; i++) {
        	DeptVO dept = dlist.get(i); //하나의 레코드(행의 정보)
        	data[i][0] = dept.getDeptno(); // 부서번호
        	data[i][1] = dept.getDname(); // 부서명
        	data[i][2] = dept.getLoc(); // 부서위치
        }
        // 부서 테이블 모델
        dmodel = new DefaultTableModel(data, colHeader1);
        
        // 뷰와 모델을 연결
        tableDept.setModel(dmodel);
    } // showDeptTable-----------------

