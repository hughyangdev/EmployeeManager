    private void btDdelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDdelActionPerformed
        String deptno = tdeptno.getText();
        if(deptno==null||deptno.trim().isEmpty()) {
        	showMsg("삭제할 부서를 테이블에서 선택하세요.");
        	return;
        }
        // 삭제 의사를 확인
        int yn = JOptionPane.showConfirmDialog(this, deptno+"번 부서를 정말 삭제할까요?",
        		"삭제확인", JOptionPane.YES_NO_OPTION);
        if(yn==JOptionPane.NO_OPTION) return;
        // deptDao의 삭제 처리 메소드 호출
        boolean result = deptDao.deleteDept(deptno);
        // 그 실행 결과 메시지 처리(삭제 성공 or 실패)
        String message = (result)? "삭제 성공":"삭제 실패-해당 부서에 소속된 직원이 존재합니다.";
        showMsg(message);
        // 모든 부서정보 가져오기
        this.showAllDeptInfo();
    }//-----------------------------

    private void btEeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEeditActionPerformed
        // 1. 수정할 값들 얻어오기
    	String empno = tempno.getText();
    	String ename = tename.getText();
    	String dinfo = cdname.getSelectedItem().toString();
    	String []token = dinfo.split("\\:");
    	String deptnoStr = token[0].trim();
    	String dname = token[1].trim();
    	
    	String job = tjob.getText();
    	String comm = tcomm.getText();
    	String sal = tsal.getText();
    	// 2. 유효성 체크
    	if(empno==null||ename==null||
    			empno.trim().isEmpty()||ename.trim().isEmpty()) {
    		showMsg("수정할 사원을 사원정보 테이블에서 선택하세요");
    		return;
    	}
    	// 3. 해당 값들을 EmpVO 객체에 담아주기
    	int empno2 = Integer.parseInt(empno.trim());
    	int deptno2 = Integer.parseInt(deptnoStr);
    	int sal2 = Integer.parseInt(sal.trim());
    	int comm2 = Integer.parseInt(comm.trim());
    	EmpVO emp = new EmpVO(empno2, ename, job, 7788, null, sal2, comm2, deptno2, null);
    	// 4. empDao의 updateEmp() 호출하기
    	boolean result = empDao.updateEmp(emp);
    	String message = (result)? "수정 성공": "수정 실패";
    	// 5. 그 결과 메시지 처리
    	showMsg(message);
    	// 6. 모든 사원정보 다시 가져와 보여주기
    	this.showAllEmpInfo();
    }//-----------------------------

    private void btFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFindActionPerformed
        String fname = JOptionPane.showInputDialog("검색할 사원의 이름을 입력하세요");
        if(fname==null) return;
        ArrayList<EmpVO> arr = empDao.findEmp(fname);
        if(arr!=null && arr.size()>0)
        	this.showEmpTable(arr);
        else showMsg("검색 정보가 없습니다.");
    }//-----------------------------

    private void btEdelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEdelActionPerformed
        // 1. 삭제할 회원번호를 tempno로부터 얻어오기
    	String empno = tempno.getText();
    	if(empno==null||empno.trim().isEmpty()) {
        	showMsg("삭제할 사원을 테이블에서 선택하세요.");
        	return;
        }
    	// 2. Confirm 대화상자를 띄워 정말 삭제하겠냐고 확인
        int yn = JOptionPane.showConfirmDialog(this, empno+"번 사원을 정말 삭제하시겠어요?",
        		"삭제 확인", JOptionPane.YES_NO_OPTION);
        if (yn==JOptionPane.NO_OPTION) return;
        // 3. 삭제하겠다고 하면 empDao의 deleteEmp() 호출
        boolean result = empDao.deleteEmp(empno);
        // 4. 그 결과 메시지 처리
        String message = (result)? "삭제 성공": "삭제 실패";
        showMsg(message);
        // 5. 모든 사원 정보 가져오기
        this.showAllEmpInfo();
    }//-----------------------------

