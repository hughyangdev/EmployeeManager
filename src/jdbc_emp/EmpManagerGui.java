	private void showEmpTable(ArrayList<EmpVO> empList) {
		Object [][]data = new Object[empList.size()][6];
		for(int i=0; i<empList.size(); i++) {
			EmpVO emp = empList.get(i); //하나의 행(레코드)
			data[i][0] = emp.getEmpno();
			data[i][1] = emp.getEname();
			data[i][2] = emp.getDeptno();
			data[i][3] = emp.getDname();
			data[i][4] = emp.getJob();
			data[i][5] = emp.getHiredate();
		} // for------------
		this.emodel = new DefaultTableModel(data, colHeader2);
		tableEmp.setModel(emodel);
	} //------------------------

	private void btEaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEaddActionPerformed
        // 1. 이름, 부서명, 업무, 급여, 보너스 값 받아오기
    	String ename = this.tename.getText();
        String dinfo = this.cdname.getSelectedItem().toString();
        // setTitle(dinfo);
        
        // ':' 을 기준으로 문자열을 쪼개어 배열로 반환한다.
        String token[] = dinfo.split("\\:");
        // token[0] => 부서번호, token[1] => 부서명
        //setTitle(token[0]);
        String deptno = (token[0]!=null)? token[0].trim(): "";
        String dname = (token[1]!=null)? token[1].trim(): "";
        
        String job = this.tjob.getText();
        String sal = this.tsal.getText();
        String comm = this.tcomm.getText();
        
        // 2. 유효성 체크(not null 컬럼은 필히 체크)
        if(ename==null||dinfo==null||ename.trim().isEmpty()||dinfo.trim().isEmpty()) {
        	showMsg("사원명과 부서선택은 필수 입력사항입니다.");
        	tename.requestFocus();
        	return;
        }
        
        int sal2 = Integer.parseInt(sal);
        int deptno2 = Integer.parseInt(deptno);
        int comm2 = (deptno2==30)? Integer.parseInt(comm): 0;
        
        // 3. 받아온 값으로 EmpVO 객체 생성
        EmpVO emp = new EmpVO(0, ename, job, 7788, null, sal2, comm2, deptno2, dname);
                
        // 4. empDao의 insertEmp() 호출
        boolean result = empDao.insertEmp(emp);
        
        // 5. 그 실행 결과 메시지 처리
        String message = (result)? "사원 정보 등록 성공": "등록 실패";
        showMsg(message);
        showAllEmpInfo();
        
    }//-------------------------

